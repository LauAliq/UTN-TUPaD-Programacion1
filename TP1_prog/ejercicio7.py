num1 = int(input("Ingrese un número entero distinto de cero: "))
num2 = int(input("Ingrese otro número entero distinto de cero: "))
sum = num1 + num2
res = num1 - num2
prod = num1 * num2
div = num1 / num2
print(f"El resultado de sumar {num1} y {num2} es {sum}.")
print(f"El resultado de restar {num1} y {num2} es {res}.")
print(f"El resultado de multiplicar {num1} y {num2} es {prod}.")
print(f"El resultado de dividir {num1} por {num2} es {div}.")